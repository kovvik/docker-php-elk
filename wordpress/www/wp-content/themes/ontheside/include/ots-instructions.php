<h3>The purpose of the custom front page is to provide a format in which to display any object or image as the center-of-attention.</h3><h4>This may entail a few bumps and work-arounds for some, yet it is a part of <em>On The Side</em> Theme styles and should be easy to configure as follows:</h4>

<ul style="list-style: inside disc; font-size: 1.114em;">
<li>You can simply add a Widget to the "Center Widget" To override any Home Page content.</li>
<li>If you do not have a widget set for "Center Widget" then your normal content will display on your Home Page.</li>
<li>If you add a widget to the Front Page and your theme is set to show Posts on the Home Page then the widget will NOT over-ride your Posts.</li>
</ul>

    <blockquote>A great solution is to put the widget: <u>"Recent Posts."</u> into Center Sidebar. <br />
    This gives you your pages on the left sidebar with plenty of open space to<br />
    show image background while <u>listing your Posts as a Table of Contents.</u></blockquote>

<h4>Side Menu Instructions </h4>
<ul style="list-style: inside disc; font-size: 1.114em;">
<li>The default widget, on the left, will show your Pages and a Log In Meta box.</li>
<li>Creating a new "Left Sidebar" widget from WP Admin will take precedence over these defaults but, still leave the Pages widget, underneath the logo, to display your blog pages.</li>
<li>This Left Sidebar menu is called from wp-nav-menu and IS the main menu.</li>   
</ul>
